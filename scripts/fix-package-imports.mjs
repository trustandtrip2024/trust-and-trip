#!/usr/bin/env node
// Repairs imperfect data left by the 2026-05-01 PDF import + 2026-05-04
// taxonomy migration on Sanity packages. Idempotent — safe to re-run.
//
// Defects this script repairs:
//   1. categoryRefs[] items missing `_type: "reference"`
//      (cleanup-redundant-categories.mjs queried partial fields and wrote
//       the filtered array back, stripping the type discriminator)
//   2. faqs[] items missing `_key` (Sanity Studio cannot edit the list)
//   3. legacy `categories` / `tags` fields lingering (set to null or [] by
//      earlier scripts; should be unset entirely)
//   4. Titles carrying redundant " · N Nights / D Days" suffix
//      (the duration / nights / days fields hold this separately)
//   5. Slugs carrying `-<pdfNum>` suffix and the cleaned title's N/D segment
//      (auto-generated by upload-pdf-packages.mjs line 663)
//
// Defaults to DRY-RUN. Add --commit to push patches.
// Add --skip-slugs to leave slugs intact (run only #1-4).
//
// On --commit, emits scripts/slug-redirects.json — a {oldSlug: newSlug}
// map consumed by next.config.js to keep any pre-rename links working.

import fs from "node:fs";
import path from "node:path";

const ENV_PATH = path.resolve(process.cwd(), ".env.local");
if (fs.existsSync(ENV_PATH)) {
  for (const line of fs.readFileSync(ENV_PATH, "utf8").split("\n")) {
    const m = line.match(/^([A-Z0-9_]+)=(.*)$/);
    if (!m) continue;
    let [, k, v] = m;
    v = v.trim().replace(/^"|"$/g, "");
    if (!process.env[k]) process.env[k] = v;
  }
}

const PROJECT_ID = process.env.NEXT_PUBLIC_SANITY_PROJECT_ID;
const DATASET = process.env.NEXT_PUBLIC_SANITY_DATASET || "production";
const TOKEN = process.env.SANITY_API_WRITE_TOKEN;
const API_HOST = `https://${PROJECT_ID}.api.sanity.io/v2024-01-01`;
const QUERY_HOST = `https://${PROJECT_ID}.api.sanity.io/v2024-01-01`;
const COMMIT = process.argv.includes("--commit");
const SKIP_SLUGS = process.argv.includes("--skip-slugs");
const REDIRECTS_OUT = path.resolve(process.cwd(), "scripts", "slug-redirects.json");

if (!PROJECT_ID || !TOKEN) {
  console.error("Missing NEXT_PUBLIC_SANITY_PROJECT_ID or SANITY_API_WRITE_TOKEN.");
  process.exit(1);
}

async function fetchQuery(query) {
  const url = `${QUERY_HOST}/data/query/${DATASET}?query=${encodeURIComponent(query)}`;
  const res = await fetch(url, { headers: { Authorization: `Bearer ${TOKEN}` } });
  if (!res.ok) throw new Error(`Query failed ${res.status}: ${await res.text()}`);
  return (await res.json()).result;
}

async function mutate(mutations, label) {
  if (!COMMIT) {
    console.log(`  [${label}] dry-run: would send ${mutations.length} mutation(s)`);
    return { results: [] };
  }
  const url = `${API_HOST}/data/mutate/${DATASET}`;
  const res = await fetch(url, {
    method: "POST",
    headers: { Authorization: `Bearer ${TOKEN}`, "Content-Type": "application/json" },
    body: JSON.stringify({ mutations }),
  });
  const json = await res.json().catch(() => ({}));
  if (!res.ok) {
    console.error(`  [${label}] FAILED ${res.status}:`, JSON.stringify(json, null, 2));
    process.exit(1);
  }
  console.log(`  [${label}] committed ${(json.results || []).length} patch(es)`);
  return json;
}

function chunk(arr, size) {
  const out = [];
  for (let i = 0; i < arr.length; i += size) out.push(arr.slice(i, i + size));
  return out;
}

function slugify(s) {
  return String(s).toLowerCase()
    .replace(/[^a-z0-9\s-]/g, " ")
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "")
    .slice(0, 90);
}

// Strip the duration phrasing the PDF import baked into titles.
// Examples:
//   "Phu Quoc Island Escape · 6 Nights / 7 Days" → "Phu Quoc Island Escape"
//   "Bali Honeymoon — 5N6D"                     → "Bali Honeymoon"
//   "Goa 4N5D"                                  → "Goa"
//   "Kerala Family Trip 7 Days"                 → "Kerala Family Trip"
function cleanTitle(raw) {
  if (!raw) return raw;
  let t = String(raw).trim();
  // " · 6 Nights / 7 Days" or " · 6N / 7D" or " — 5N6D"
  t = t.replace(/\s*[·—–\-|]\s*\d{1,2}\s*N(?:ights?)?\s*\/?\s*\d{1,2}\s*D(?:ays?)?\s*$/i, "");
  // trailing "5N6D" / "5 Nights 6 Days" without separator
  t = t.replace(/\s+\d{1,2}\s*N(?:ights?)?\s*\/?\s*\d{1,2}\s*D(?:ays?)?\s*$/i, "");
  // trailing "6 Nights" or "7 Days" alone
  t = t.replace(/\s+\d{1,2}\s*(?:Nights?|Days?)\s*$/i, "");
  // collapse double-separators left behind
  t = t.replace(/\s*[·—–|]\s*$/g, "").replace(/\s{2,}/g, " ").trim();
  return t || raw;
}

async function main() {
  console.log(`Repair starting on ${PROJECT_ID}/${DATASET}${COMMIT ? "" : " (DRY RUN)"}`);

  // ────────────────────────────────────────────────────────────────────
  // Pull every package once. Re-use the snapshot for all 5 patch passes
  // so we make exactly one read of the dataset.
  // ────────────────────────────────────────────────────────────────────
  const packages = await fetchQuery(`*[_type == "package"]{
    _id,
    title,
    "slugCurrent": slug.current,
    "categoriesPresent": defined(categories),
    "tagsPresent": defined(tags),
    categoryRefs,
    tagRefs,
    faqs,
    nights,
    days
  }`);
  console.log(`\nFetched ${packages.length} packages.`);
  const isImported = (p) => typeof p._id === "string" && p._id.startsWith("imported-pdf-");

  // ────────────────────────────────────────────────────────────────────
  // Pass 1: re-add `_type: "reference"` on categoryRefs[] items missing it.
  // ────────────────────────────────────────────────────────────────────
  const fixCatRefs = [];
  for (const p of packages) {
    const refs = Array.isArray(p.categoryRefs) ? p.categoryRefs : null;
    if (!refs?.length) continue;
    const broken = refs.some((r) => r && !r._type);
    if (!broken) continue;
    const repaired = refs.map((r) => ({
      _key: r._key || `cref-${slugify(r._ref || "x")}`,
      _type: "reference",
      _ref: r._ref,
    }));
    fixCatRefs.push({ patch: { id: p._id, set: { categoryRefs: repaired } } });
  }
  console.log(`\n[1/5] categoryRefs missing _type: ${fixCatRefs.length} doc(s)`);
  for (const batch of chunk(fixCatRefs, 50)) await mutate(batch, "catRefs");

  // ────────────────────────────────────────────────────────────────────
  // Pass 2: inject `_key` into every faqs[] item that lacks one.
  // ────────────────────────────────────────────────────────────────────
  const fixFaqs = [];
  for (const p of packages) {
    const faqs = Array.isArray(p.faqs) ? p.faqs : null;
    if (!faqs?.length) continue;
    const missing = faqs.some((f) => f && !f._key);
    if (!missing) continue;
    const seen = new Set();
    const repaired = faqs.map((f, i) => {
      let key = f?._key || `faq-${i}-${slugify(f?.q || "q").slice(0, 24)}`;
      let n = 0;
      while (seen.has(key)) key = `${key}-${++n}`;
      seen.add(key);
      return { _key: key, ...(f || {}) };
    });
    fixFaqs.push({ patch: { id: p._id, set: { faqs: repaired } } });
  }
  console.log(`\n[2/5] faqs missing _key: ${fixFaqs.length} doc(s)`);
  for (const batch of chunk(fixFaqs, 50)) await mutate(batch, "faqs");

  // ────────────────────────────────────────────────────────────────────
  // Pass 3: unset legacy `categories` + `tags` ghost fields when the new
  // ref-backed field is populated. Treat null/[] as legacy too.
  // ────────────────────────────────────────────────────────────────────
  const fixGhost = [];
  for (const p of packages) {
    const unset = [];
    // Use the server-side `defined(...)` flag — `null` projections come
    // back as `null` regardless of whether the field actually exists,
    // so a JS hasOwnProperty check would over-match.
    if (p.categoriesPresent) unset.push("categories");
    if (p.tagsPresent) unset.push("tags");
    if (!unset.length) continue;
    fixGhost.push({ patch: { id: p._id, unset } });
  }
  console.log(`\n[3/5] legacy categories/tags ghost fields: ${fixGhost.length} doc(s)`);
  for (const batch of chunk(fixGhost, 50)) await mutate(batch, "ghost");

  // ────────────────────────────────────────────────────────────────────
  // Pass 4: clean title — strip "· N Nights / D Days" trailing duration.
  // Only touches imported-pdf-* docs; hand-curated titles may legitimately
  // end with a duration phrase.
  // ────────────────────────────────────────────────────────────────────
  const fixTitles = [];
  for (const p of packages) {
    if (!isImported(p)) continue;
    const cleaned = cleanTitle(p.title);
    if (cleaned === p.title) continue;
    if (!cleaned || cleaned.length < 3) continue;
    fixTitles.push({ patch: { id: p._id, set: { title: cleaned } } });
  }
  console.log(`\n[4/5] titles carrying duration suffix: ${fixTitles.length} doc(s)`);
  for (const batch of chunk(fixTitles, 50)) await mutate(batch, "titles");

  // ────────────────────────────────────────────────────────────────────
  // Pass 5: rebuild slugs from cleaned titles. Drop -<pdfNum> trailers.
  // Emit a {oldSlug: newSlug} map for redirect wiring.
  // ────────────────────────────────────────────────────────────────────
  if (SKIP_SLUGS) {
    console.log(`\n[5/5] slugs: SKIPPED (--skip-slugs)`);
  } else {
    // Only rewrite slugs for imported-pdf-* docs. Hand-curated package
    // slugs already look fine and may not match a slugified-title pattern.
    const used = new Set(
      packages
        .map((p) => p.slugCurrent)
        .filter(Boolean)
    );
    const redirectMap = {};
    const fixSlugs = [];

    for (const p of packages) {
      if (!isImported(p)) continue;
      const oldSlug = p.slugCurrent;
      if (!oldSlug) continue;
      const cleanedTitle = cleanTitle(p.title) || p.title;
      const candidate = slugify(cleanedTitle);
      if (!candidate) continue;
      if (candidate === oldSlug) continue;

      // collision-safe dedupe — if the cleaned slug is already taken by
      // another doc, append -2, -3, …
      used.delete(oldSlug);
      let final = candidate;
      let n = 1;
      while (used.has(final)) {
        n += 1;
        final = `${candidate}-${n}`;
      }
      used.add(final);

      if (final === oldSlug) continue;
      redirectMap[oldSlug] = final;
      fixSlugs.push({
        patch: {
          id: p._id,
          set: { slug: { _type: "slug", current: final } },
        },
      });
    }

    console.log(`\n[5/5] slugs to rewrite: ${fixSlugs.length} doc(s)`);
    if (fixSlugs.length && COMMIT) {
      fs.writeFileSync(REDIRECTS_OUT, JSON.stringify(redirectMap, null, 2));
      console.log(`     wrote redirect map → ${path.relative(process.cwd(), REDIRECTS_OUT)}`);
    } else if (fixSlugs.length) {
      console.log(`     sample mappings:`);
      for (const [from, to] of Object.entries(redirectMap).slice(0, 5)) {
        console.log(`       ${from}\n          → ${to}`);
      }
    }
    for (const batch of chunk(fixSlugs, 50)) await mutate(batch, "slugs");
  }

  console.log(`\nDone${COMMIT ? "" : " (dry run — re-run with --commit to apply)"}.`);
}

main().catch((err) => { console.error("Repair failed:", err); process.exit(1); });
